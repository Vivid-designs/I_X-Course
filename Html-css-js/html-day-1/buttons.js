function toggleColorGrid() {
    const colorGrid = document.getElementById('color-grid');
    colorGrid.style.display = colorGrid.style.display === 'none' ? 'grid' : 'none';
}

function toggleBootstrapGrid() {
    const bootstrapGrid = document.getElementById('bootstrap-grid');
    bootstrapGrid.style.display = bootstrapGrid.style.display === 'none' ? 'block' : 'none';
}
